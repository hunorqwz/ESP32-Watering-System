import { Client } from '@neondatabase/serverless';
import fs from 'fs';

const env = fs.readFileSync('.env', 'utf8');
const config = {};
env.split('\n').forEach(l => {
  const parts = l.split('=');
  if (parts.length >= 2) {
    config[parts[0].trim()] = parts.slice(1).join('=').trim();
  }
});

const connectionString = config.DATABASE_URL;
const baseUrl = 'http://127.0.0.1:3000';

async function main() {
  console.log('--- STARTING SOIL MOISTURE SKIP INTEGRATION TESTS ---');

  const client = new Client({ connectionString });
  await client.connect();

  let createdScheduleId = null;
  let createdReadingId = null;
  let originalThreshold = null;

  try {
    // Temporarily bind sensor 1 to Pump 1
    console.log('[Setup] Binding sensor 1 to Pump 1...');
    await client.query("UPDATE sensor_configs SET pump_id = 1 WHERE id = 1");

    // 1. Get current moisture threshold to restore later
    const thRes = await client.query("SELECT value FROM system_config WHERE key = 'moisture_skip_threshold_percent'");
    originalThreshold = thRes.rows[0]?.value || '70';
    console.log(`[Config] Original moisture skip threshold: ${originalThreshold}%`);

    // Set threshold to 15% for the test (since average with other dry sensors is ~28%)
    await client.query("UPDATE system_config SET value = '15' WHERE key = 'moisture_skip_threshold_percent'");

    // 2. Set up a test schedule for Tuesday (Day 2) at 08:00
    const timeOfDay = '08:00:00';
    const localDayOfWeek = 2;
    console.log('\n[Step 1] Creating temporary schedule for Tuesday at 08:00...');
    const schedRes = await client.query(
      `INSERT INTO watering_schedules (pump_ids, time_of_day, duration_seconds, days_of_week, enabled)
       VALUES (ARRAY[1], $1, 60, ARRAY[$2::int], true) RETURNING id`,
      [timeOfDay, localDayOfWeek]
    );
    createdScheduleId = schedRes.rows[0].id;
    console.log(`Created test schedule ID: ${createdScheduleId}`);

    // Ensure pump 1 state is initially 0
    await client.query('UPDATE pump_configs SET state = 0 WHERE id = 1');

    // 3. Mock high soil moisture reading (e.g. 1100, which represents 100% moisture)
    console.log('\n[Step 2] Inserting mock high moisture reading (100% relative moisture) for sensor 1...');
    const readingRes = await client.query(
      `INSERT INTO sensor_readings (sensor_config_id, value)
       VALUES (1, 1100) RETURNING id`
    );
    createdReadingId = readingRes.rows[0].id;
    console.log(`Created mock reading ID: ${createdReadingId}`);

    // 4. Trigger Cron Run
    console.log('\n[Step 3] Executing cron runner GET /api/cron/run?simulated_time=08:00&simulated_day=2...');
    const triggerRes = await fetch(`${baseUrl}/api/cron/run?simulated_time=08:00&simulated_day=2`);
    const triggerBody = await triggerRes.json();
    console.log('Cron response:', triggerBody);

    if (triggerRes.status !== 200 || !triggerBody.success) {
      throw new Error('Cron execution failed.');
    }

    // Verify that Pump 1 was skipped
    const trigger = triggerBody.triggers.find(t => t.pumpId === 1);
    if (!trigger) {
      throw new Error('Pump 1 was not evaluated in triggers list.');
    }
    
    if (trigger.status !== 'skipped') {
      throw new Error(`Expected trigger status 'skipped' but got '${trigger.status}'.`);
    }

    if (!trigger.reason.includes('Soil moisture is high')) {
      throw new Error(`Expected skip reason containing 'Soil moisture is high' but got '${trigger.reason}'.`);
    }

    console.log('SUCCESS: Watering cycle was successfully skipped due to high soil moisture!');

    // Check command_logs table to verify skipped log is recorded in database
    const logRes = await client.query(
      `SELECT error_details FROM command_logs 
       WHERE pump = 1 AND state = 1 AND status = 'skipped' 
       ORDER BY created_at DESC LIMIT 1`
    );
    console.log('Last command log skipped message:', logRes.rows[0]?.error_details);
    if (!logRes.rows[0]?.error_details.includes('Moisture skip active')) {
      throw new Error('No corresponding skipped log record found in command_logs.');
    }
    console.log('SUCCESS: Skip log was successfully persisted to command_logs database table!');

  } catch (err) {
    console.error('\nTEST FAILED:', err.message);
    process.exitCode = 1;
  } finally {
    console.log('\n[Cleanup] Cleaning up database test records...');
    // Unbind sensor 1 from Pump 1
    await client.query("UPDATE sensor_configs SET pump_id = NULL WHERE id = 1");

    if (createdScheduleId) {
      await client.query('DELETE FROM watering_schedules WHERE id = $1', [createdScheduleId]);
    }
    if (createdReadingId) {
      await client.query('DELETE FROM sensor_readings WHERE id = $1', [createdReadingId]);
    }
    if (originalThreshold !== null) {
      await client.query('UPDATE system_config SET value = $1 WHERE key = $2', [originalThreshold, 'moisture_skip_threshold_percent']);
    }
    // Delete test command log records generated by this run
    await client.query("DELETE FROM command_logs WHERE pump = 1 AND created_at > NOW() - INTERVAL '15 minutes'");
    await client.end();
    console.log('--- TEST RUN COMPLETED ---');
  }
}

main();
